export function getGameInitials(name: string): string {
  const parts = name.trim().split(/[\s:_-]+/);
  if (parts.length === 1) return parts[0].substring(0, 3).toUpperCase();
  return (parts[0][0] + (parts[1]?.[0] || "")).toUpperCase();
}

